public class Contato {
    private String Nome;
    private String Telefone;

    public Contato(String nome, String telefone){
        this.Nome = nome;
        this.Telefone = telefone;
    }

    public String getNome(){
        return this.Nome;
    }

    public String getTelefone(){
        return this.Telefone;   
    }
}

    
